    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            input = new BufferedReader(new InputStreamReader(socket.getInputStream()));
            output = new PrintStream(socket.getOutputStream());
            new Thread(new Runnable(){
                @Override
                public void run() {
                    while(true){
                        try {
                            Thread.sleep(100);
                            String message = input.readLine();
                            if(message.startsWith("/accept")){
                                MenuUtama menu = new MenuUtama();
                                menu.setUsername(username);
                                menu.setSocket(socket);
                                menu.setVisible(true);
                                close();
                                break;
                            }
                            else if(message.startsWith("/soalBaruTerbuat")){
                                JOptionPane.showMessageDialog(null, "Soal berhasil dibuat");
                            }
                            else if(message.startsWith("/soalBaruGagalTerbuat")){
                                JOptionPane.showMessageDialog(null, "Gagal membuat soal");
                            }
                        } catch (InterruptedException | IOException ex) {
                            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }).start();
        } catch (IOException ex) {
            Logger.getLogger(MenuUtama.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

