    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        boolean kondisi =true;
        String jawaban="";
        String a="";
        String b="";
        String c="";
        String d="";
        String soal="";
        
        String error="";
        if(!tarSoal.getText().trim().isEmpty())
            soal = tarSoal.getText();
        else{
            kondisi=false;
            error+="Isi dulu soalnya\n";
        }
        if(!txtA.getText().trim().isEmpty())
            a=txtA.getText();
        else {
            kondisi=false;
            error+="Isi dulu textfield A\n";
        }
        if(!txtB.getText().trim().isEmpty())
            b=txtB.getText();
        else {
            kondisi=false;
            error+= "Isi dulu textfield B\n";
        }
        if(!txtC.getText().trim().isEmpty())
            c=txtC.getText();
        else {
            kondisi=false;
            error+="Isi dulu textfield C\n";
        }
        if(!txtD.getText().trim().isEmpty())
            d=txtD.getText();
        else {
            kondisi=false;
            error+= "Isi dulu textfield D\n";
        }
        
        
        if(rdoA.isSelected())
            jawaban="A";
        else if(rdoB.isSelected())
            jawaban="B";
        else if(rdoC.isSelected())
            jawaban="C";
        else if(rdoD.isSelected())
            jawaban="D";
        else{
            kondisi=false;
            error+="Pilih jawaban benar\n";
        }
        if(kondisi){
            try {
                output = new PrintStream(socket.getOutputStream());
                output.println("/simpanSoalBaru;"+soal+";"+a+";"+b+";"+c+";"+d+";"+jawaban);
            } catch (IOException ex) {
                Logger.getLogger(MenuUtama.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
            JOptionPane.showMessageDialog(null, error);
    }//GEN-LAST:event_btnTambahActionPerformed

